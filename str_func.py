def text_upper():
    """ Функция перевода всех букв в тексте в заглавные  """
    user_text = input("Введите текст ")
    return user_text.upper()


def text_title():
    """ Функция перевода первых букв слова в тексте в заглавные  """
    user_text = input("Введите текст ")
    return user_text.title()
